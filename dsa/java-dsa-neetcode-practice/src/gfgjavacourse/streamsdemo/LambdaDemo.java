public class LambdaDemo {

    /**
     * Lambda Expressions Syntax
     * A functional interface which exactly has 1 abstract method
     * Single Line void Lambdas -
     * No parameter : () -> sout("hello");
     * Single param : p -> sout(p)
     * Multi param : (p1, p2) -> sout(p1 + p2);
     * Multiline : p -> {}
     *
     * ==============================================================
     *
     * In functional interface we can pass lambda expression
     *
     * works great with lambda expressions and method reference
     * can be parallelized
     *
     * */
}
