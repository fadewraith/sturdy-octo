package gfgjavacourse.stackandqueue;

public class ArrayDequeDemo {

    /**
     * push, peek, pop, offer, poll, element
     * throws exception - add(), remove(), element()
     * return null/false - offer(), poll(), peek()
     * */

    /**
     * Functions that throw exceptions -
     * addFirst(), addLast(), removeFirst(), removeLast(), getFirst(), getLast()
     *
     * Equivalent functions that do not throw -
     * offerFirst(), offerLast(), pollFirst(), pollLast(), peekFirst(), peekLast()
     * */

    /**
     * Redundant Functions -
     * add(), remove(), poll(), offer(), element(), peek(), push(), pop()
     * addLast(), removeFirst(), pollFirst(), offerLast(), getFirst(), peekFirst(), addFirst(), removeFirst()
     * */
}
